def list_visits(limit:int=Query(100,ge=1,le=500),offset:int=Query(0,ge=0),status:str|None=None,from_date:str|None=None,to_date:str|None=None):return {'items':tr(service.list_visits,limit,offset,status,from_date,to_date)}
